DROP TABLE IF EXISTS PLAYER;

DROP TABLE IF EXISTS SHIP_TYPE;

DROP TABLE IF EXISTS SHIP;

create table PLAYER
(
ID DECIMAL,
NICKNAME VARCHAR(120),
TEAM DECIMAL,
GOLD DECIMAL
);

create table SHIP_TYPE
(
ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
NAME VARCHAR(120),
MEAN_SPEED DECIMAL,
SPEED_DEVIATION DECIMAL,
MEAN_POWER DECIMAL,
POWER_DEVIATION DECIMAL,
MEAN_TONNAGE DECIMAL,
TONNAGE_DEVIATION DECIMAL
);

create table SHIP
(
ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
OWNER_ID DECIMAL,
NAME VARCHAR(120),
TYPE_ID DECIMAL,
SPEED DECIMAL,
POWER DECIMAL,
TONNAGE DECIMAL,
EMPLOYED DECIMAL,
LOCATION DECIMAL
);
